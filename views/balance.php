            <strong>Façon d'être</strong><br /><br />
            Le premier livre d'étiquette au monde aurait été écrit par une Balance. Vraiment? Le fait est que ce signe est très poli. Peut-être que certains Librans ne connaissent pas vraiment les bonnes manières, mais ils savent se comporter à tout moment, car ils ne manquent pas des bases: un mélange de gentillesse, de chaleur, de diplomatie et même un soupçon de sophistication. Il est possible de trouver tout cela en Balance, car ce signe prend soin de ces qualités en tant que musicien s'occupe du piano lui-même. C'est un outil de travail! Librans utilise ces outils pour se rapprocher des gens et établir des relations harmonieuses. Ils sont somptueux en cela: en plus de leur convivialité naturelle, ils sont communicatifs, agréables, captivants. Ils se révèlent également être des auditeurs attentifs, qui se concentrent sur les paroles de l'interlocuteur. Tout autre chose que cela semblerait loufoque, et l'impolitesse est une possibilité tout simplement impensable pour la Balance. Mais quel est le but de toute cette bonne éducation? C'est une intention illustre: semer la coopération. La Balance est née pour promouvoir les relations, équilibrer les différences, ajuster les contrastes. Dans le sillage de cela, vient l'envie de répandre la beauté! 
