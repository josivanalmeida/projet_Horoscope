            <strong>Façon d'être</strong><br /><br />
            Pourquoi le symbole de votre signe est-il une jeune fille tenant un épi de blé? On dit que ce symbole délivre la vocation élémentaire de la Vierge: séparer l'ivraie du blé! C'est à vous d'observer, d'organiser, de classer, d'évaluer, de juger. Leurs yeux intelligents et critiques ne manquent de rien, même lorsqu'ils sont positionnés devant le miroir. Oui: vous êtes une personne exigeante avec tout le monde - et encore plus avec vous-même. Une certitude règne parmi ses convictions: tout ce que touche la Vierge peut s'améliorer. D'où son souci des méthodes, son attention aux processus et sa fixation sur le travail, les heures, le travail. La Vierge travaille dur - et ne se plaint pas! En mesurant, coupant, taillant, ponçant et polissant des choses et des personnes, vous accomplissez vos autres objectifs zodiacaux: favoriser la guérison, aider à restaurer la santé. La guérison et la santé ne signifient pas ici, nécessairement, la maladie et la récupération du corps physique, mais des relations humaines et des idéaux de vie. Son intention est d'améliorer tout à sa portée, car il croit que le secret du bonheur y réside. Et cette tâche requiert de la discipline. C'est pourquoi votre enseigne a la réputation d'être bien rangée.
